"""
科研综述写作准则（共享模块）。

提炼自 Nature 写作方法论（related-work / paragraph-flow），
供 writer / synthesizer / critic / polish 各 agent 复用，改一处全生效。
"""

# ─────────── 综述结构与论证准则（related-work 方法论） ───────────
SURVEY_PRINCIPLES = """【综述论证准则】
- 按**技术主题**分组，不要按发表年份罗列。每组 2-4 个主题，例如：
  (1) 该方向的主流方法  (2) 与核心思路最接近的方法  (3) 本研究依赖的辅助技术
- 每个主题用三段式推进：
  通用范式（这组方法的共同思路）→ 关键局限（与本研究问题相关的不足）→ 区别/空白（本研究的切入点）
- 比较**机制、假设、失败模式**，而不是简单堆砌"某某也做了某事"
- 强调本研究要填补的**确切空白**，用技术语言而非营销语言
- 不要把综述写成引用列表（citation dump）；也不要回避最强的对比工作"""

# ─────────── 段落级写作工艺（paragraph-flow 方法论） ───────────
PARAGRAPH_CRAFT = """【段落写作工艺】
- 一个段落只讲**一个核心信息**，首句即点题（claim-first），让读者一眼看出本段在做什么
- 句与句之间用**显式逻辑关系**衔接：therefore / however / by contrast / as a result / for example
  （中文可用：因此 / 然而 / 相比之下 / 由此 / 例如）
- 术语、缩写**先定义再复用**，不要让读者猜
- 每段都要能映射回所在章节的主旨；放错位置的句子移走
- 用因果、对比、递进、举例推进，不要用空泛的过渡句凑字数"""

# ─────────── 给"撰写"型 agent 的完整指南 ───────────
WRITING_GUIDE = f"""{SURVEY_PRINCIPLES}

{PARAGRAPH_CRAFT}"""

# ─────────── 给 critic 的检查项（对应上面准则） ───────────
WRITING_CHECKLIST = """【写作质量检查】
- 是否按技术主题分组（而非按年份罗列）？
- 每个主题是否点明了"通用范式 → 局限 → 区别"？还是只在堆砌引用？
- 段落是否 claim-first、一段一信息？句间是否有显式逻辑衔接？
- 最强的对比工作有没有被回避？术语是否先定义再用？"""
